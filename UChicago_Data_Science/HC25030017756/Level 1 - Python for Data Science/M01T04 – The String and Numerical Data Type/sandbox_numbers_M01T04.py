
# to use the functions in the math module, add this line of code to the top of your program: 
import math

# round down
print(math.floor(30.33333))

# round up
print(math.floor(30.3333))

# cuts off the decimal
print(math.trunc(30.3333))

# finds the square root of a number
print(math.sqrt(4))

# pi
print(math.pi)

